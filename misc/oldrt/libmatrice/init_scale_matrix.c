#include "libmatrice.h"

t_matrix	*init_scale(double scale, t_matrix *t)
{
	t->x[0] = scale;
	t->x[1] = 0;
	t->x[2] = 0;
	t->x[3] = 0;
	t->y[0] = 0;
	t->y[1] = scale;
	t->y[2] = 0;
	t->y[3] = 0;
	t->z[0] = 0;
	t->z[1] = 0;
	t->z[2] = scale;
	t->z[3] = 0;
	t->w[0] = 0;
	t->w[1] = 0;
	t->w[2] = 0;
	t->w[3] = 1;
	return (t);
}
