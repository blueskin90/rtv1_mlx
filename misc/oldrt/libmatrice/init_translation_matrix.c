#include "libmatrice.h"

t_matrix		*init_translation(double x, double y, double z, t_matrix *t)
{
	t->x[0] = (t->x[0] == 0) ? 1 :  t->x[0];
	t->x[1] += 0;
	t->x[2] += 0;
	t->x[3] += x;
	t->y[0] += 0;
	t->y[1] =  (t->y[1] == 0) ?  1 :  t->y[1];
	t->y[2] += 0;
	t->y[3] += y;
	t->z[0] += 0;
	t->z[1] += 0;
	t->z[2] =  (t->z[2] == 0) ?  1 : t->z[2];
	t->z[2] += 0;
	t->z[3] += z;
	t->w[0] = 0;
	t->w[1] = 0;
	t->w[2] = 0;
	t->w[3] = 1;
	return (t);
}

